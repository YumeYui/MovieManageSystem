    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = this.selectedRow;
        int col = this.selectedCol;
        
        if (new TicketDao().where("showing_id", "=", this.showing.getId()).where("row", "=", row).where("col", "=", col).first() != null) {
            this.jLabel9.setText("该位置已售出，请重新选择空座位。");
            return;
        }
        
        TicketDao ticket = new TicketDao();
        ticket.setShowing_id(this.showing.getId());
        ticket.setRow(row);
        ticket.setCol(col);
        
        if (ticket.create() != null) {
            this.jLabel9.setText("售票成功！");
            String key = ticket.getRow() + "-" + ticket.getCol();
            this.buttonsMap.get(key).setBackground(busyColor);
            return;
        }
        
        this.jLabel9.setText("售票失败！");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        int row = this.selectedRow;
        int col = this.selectedCol;
        
        TicketDao ticket = (TicketDao) new TicketDao()
                .where("showing_id", "=", this.showing.getId())
                .where("row", "=", row)
                .where("col", "=", col).first();
        
        if (ticket == null) {
            this.jLabel9.setText("该位置为空座位，请重新选择已售出座位。");
            return;
        }
        
        if (ticket.delete()) {
            this.jLabel9.setText("退票成功！");
            String key = ticket.getRow() + "-" + ticket.getCol();
            this.buttonsMap.get(key).setBackground(freeColor);
            return;
        }
        
        this.jLabel9.setText("退票失败！");
    }//GEN-LAST:event_jButton2ActionPerformed

