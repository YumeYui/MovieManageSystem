    private void initComponentsWithData () {
        if (this.movie != null) {
            BufferedImage img = null;
            try {
                img = ImageIO.read(new URL(this.movie.getCover()));
                Image dimg = img.getScaledInstance(this.jLabel1.getWidth(), this.jLabel1.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon imageIcon = new ImageIcon(dimg);
                this.jLabel1.setIcon(imageIcon);
            } catch (IOException e) {
                e.printStackTrace();
            }
            this.jLabel2.setText(this.movie.getName());
            this.jLabel3.setText("导演：" + this.movie.getDirector());
            this.jLabel4.setText("主演：" + this.movie.getStarring());
            this.jLabel5.setText("类型：" + this.movie.getType());
            this.jLabel6.setText("票价：" + this.movie.getPrice());
        }
        
        if (this.room != null) {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            this.jLabel7.setText("放映时间：" + sdf.format(this.showing.getDate()));
            this.jLabel8.setText("放映厅：" + this.room.getName());
            
            // init seats
            int rows = this.room.getRows().intValue();
            int cols = this.room.getColumns().intValue();
            jPanel2.setLayout(new java.awt.GridLayout(rows, cols, 8, 8));
            

            for (int i = 1; i <= rows; i++) {
                for (int j = 1; j <= cols; j++) {
                    String key = i + "-" + j;
                    JButton seat = new JButton(key);
                    seat.setBackground(freeColor);
                    seat.setForeground(textColor);
                    seat.setFont(new Font("Arial", Font.BOLD, 20));
                    seat.addActionListener(new SeatClickLinstener(i, j));
                    
                    buttonsMap.put(key, seat);
                    jPanel2.add(seat);
                }
            }
        }
        
        // render button as busy color
        if (this.soldTickets != null) {
            for (TicketDao soldTicket : soldTickets) {
                String key = soldTicket.getRow() + "-" + soldTicket.getCol();
                this.buttonsMap.get(key).setBackground(busyColor);
            }
        }
    }
    
    /**
     * select seat evnet linstener
     */
    private class SeatClickLinstener implements ActionListener {
        private int row;
        private int col;

        public SeatClickLinstener(int row, int col) {
            this.row = row;
            this.col = col;
        }
        
        @Override
        public void actionPerformed(ActionEvent event) {
            TicketSelling.this.selectedRow = this.row;
            TicketSelling.this.selectedCol = this.col;
            TicketSelling.this.jLabel10.setText("当前选择座位：" + this.row + "-" + this.col);
        }
    }
    
