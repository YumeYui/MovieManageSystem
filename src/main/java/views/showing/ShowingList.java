    public void fillTable() {
        this.tableModel.setRowCount(0);

        String id = this.jTextField1.getText();
        String date = this.jTextField2.getText();
        String movie_id = this.jTextField3.getText();
        String room_id = this.jTextField4.getText();

        ShowingDao showingDao = new ShowingDao();

        if (id.length() > 0) {
            showingDao.where("id", "=", Long.valueOf(id));
        }

        if (date.length() > 0) {
            showingDao.where("date", "like", "%" + date + "%");
        }

        if (movie_id.length() > 0) {
            showingDao.where("movie_id", "=", Long.valueOf(movie_id));
        }

        if (room_id.length() > 0) {
            showingDao.where("room_id", "=", Long.valueOf(room_id));
        }

        this.showings = showingDao.get();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        for (ShowingDao showing : this.showings) {
            Object[] row = {
                showing.getId(),
                sdf.format(showing.getDate()),
                showing.getMovie_id(),
                showing.getRoom_id()
            };
            this.tableModel.addRow(row);
        }
    }
