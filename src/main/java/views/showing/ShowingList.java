    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        this.jLabel5.setText("");
        int selected = this.jTable1.getSelectedRow();
        if (selected >= 0) {
            
            ShowingDao showing = this.showings.get(selected);
            ShowingList _this = this;
            java.awt.EventQueue.invokeLater(new Runnable() {
                public void run() {
                    new ShowingEdit(_this, showing).setVisible(true);
                }
            });
        } else {
            this.jLabel5.setText("请先选择要修改的数据");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        this.jLabel5.setText("");
        int selected = this.jTable1.getSelectedRow();
        if (selected >= 0) {
            ShowingDao showing = this.showings.get(selected);
            if (showing.delete()) {
                this.tableModel.removeRow(selected);
                this.jLabel5.setText("数据已删除");
            } else {
                this.jLabel5.setText("数据删除失败");
            }
        } else {
            this.jLabel5.setText("请先选择要删除的数据");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

