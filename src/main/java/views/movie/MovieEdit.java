    private void fill () {
        if (this.movie != null) {
            this.isNew = false;
            
            this.jLabel8.setText(String.valueOf(this.movie.getId()));
            this.jTextField1.setText(this.movie.getName());
            this.jTextField2.setText(this.movie.getDirector());
            this.jTextField3.setText(this.movie.getStarring());
            this.jTextField4.setText(this.movie.getCover());
            this.jTextField5.setText(this.movie.getType());
            this.jFormattedTextField1.setText(String.valueOf(this.movie.getPrice()));
        }
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        this.jLabel9.setText("");
        String name = this.jTextField1.getText();
        String director = this.jTextField2.getText();
        String starring = this.jTextField3.getText();
        String cover = this.jTextField4.getText();
        String type = this.jTextField5.getText();
        String price = this.jFormattedTextField1.getText();
        
        if (name.length() <= 0) {
            this.jLabel9.setText("电影名不能为空");
            return;
        }
        
        if (director.length() <= 0) {
            this.jLabel9.setText("导演不能为空");
            return;
        }
        
        if (starring.length() <= 0) {
            this.jLabel9.setText("主演不能为空");
            return;
        }
        
        if (cover.length() <= 0) {
            this.jLabel9.setText("封面图片不能为空");
            return;
        }
        
        if (!Validator.isURL(cover)) {
            this.jLabel9.setText("封面图片链接格式不正确");
            return;
        }
        
        if (type.length() <= 0) {
            this.jLabel9.setText("电影类型不能为空");
            return;
        }
        
        if (price.length() <= 0) {
            this.jLabel9.setText("票价不能为空");
            return;
        }
        
        Float floatPrice = Float.valueOf(price);        
        if (floatPrice <= 0) {
            this.jLabel9.setText("票价必须为正数");
            return;
        }
        
        MovieDao movie = this.isNew ? new MovieDao() : this.movie;
        
        movie.setName(name);
        movie.setDirector(director);
        movie.setStarring(starring);
        movie.setCover(cover);
        movie.setType(type);
        movie.setPrice(BigDecimal.valueOf(floatPrice));
        
        if (movie.save() != null) {
            if (isNew) {
                this.jLabel8.setText(String.valueOf(movie.getId()));
                this.jLabel9.setText("电影创建成功");
            } else {
                this.jLabel9.setText("电影更新成功");
            }
            return;
        }
        
        this.jLabel9.setText("电影更新失败");
    }//GEN-LAST:event_jButton1ActionPerformed

