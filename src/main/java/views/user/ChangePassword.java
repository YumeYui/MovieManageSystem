    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        this.jLabel4.setText("");
        
        String oldPassword = String.valueOf(this.jPasswordField1.getPassword());
        String newPassword = String.valueOf(this.jPasswordField2.getPassword());
        String confirmPassword = String.valueOf(this.jPasswordField3.getPassword());
        
        UserDao user = Global.user;
        
        if (!user.authorize(oldPassword)) {
            this.jLabel4.setText("密码不正确");
            return;
        }
        
        if(newPassword.length() <= 0) {
            this.jLabel4.setText("密码不能为空");
            return;
        }

        if (newPassword.length() > 20) {
            this.jLabel4.setText("密码长度不能超过 20 字节");
            return;
        }

        if (oldPassword.equals(newPassword)) {
            this.jLabel4.setText("新旧密码一致");
            return;
        }
        
        if(confirmPassword.length() <= 0) {
            this.jLabel4.setText("密码不能为空");
            return;
        }

        if (!newPassword.equals(confirmPassword)) {
            this.jLabel4.setText("两次输入的密码不一致");
            return;
        }
        
        user.setPassword(Hash.encrypt(newPassword));
        if (user.save() != null) {
            this.jLabel4.setText("密码修改成功");
            this.jPasswordField1.setText("");
            this.jPasswordField2.setText("");
            this.jPasswordField3.setText("");
            return;
        }
        this.jLabel4.setText("密码修改失败");
    }//GEN-LAST:event_jButton1ActionPerformed

